//! Static model capacity fallback catalog.
//!
//! See `docs/model-catalog.md` for source, refresh, and boundary rules.

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub struct ModelCapacity {
    pub context_window_tokens: usize,
    pub max_completion_tokens: usize,
}

pub const CONSERVATIVE_CONTEXT_WINDOW_TOKENS: usize = 32_768;
pub const CONSERVATIVE_MAX_COMPLETION_TOKENS: usize =
    crate::context_budget::DEFAULT_MAX_COMPLETION_TOKENS;

pub fn conservative_model_capacity() -> ModelCapacity {
    ModelCapacity {
        context_window_tokens: CONSERVATIVE_CONTEXT_WINDOW_TOKENS,
        max_completion_tokens: CONSERVATIVE_MAX_COMPLETION_TOKENS,
    }
}

pub fn catalog_model_capacity(model_id: &str) -> Option<ModelCapacity> {
    let normalized = model_id.trim().to_ascii_lowercase();
    let index = LITELLM_MODEL_CAPACITIES
        .binary_search_by(|entry| entry.0.cmp(normalized.as_str()))
        .ok()?;
    let (_, context_window_tokens, max_completion_tokens) = LITELLM_MODEL_CAPACITIES[index];
    Some(ModelCapacity {
        context_window_tokens,
        max_completion_tokens,
    })
}

